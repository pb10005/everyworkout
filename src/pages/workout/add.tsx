import { useState, useCallback } from "react";
import { type NextPage } from "next";
import Head from "next/head";
import { useSession } from "next-auth/react";
import Router from "next/router";

import {
  Button,
  Navigation,
  ExerciseSelector,
  Loading,
} from "../../components";
import { api } from "../../utils/api";

const AddWorkout: NextPage = () => {
  const { data: sessionData } = useSession();
  const [date, setDate] = useState<string>(
    new Date().toISOString().split("T")[0] || ""
  );
  const [selectedExerciseId, selectExerciseId] = useState(-1);
  const [weight, setWeight] = useState<string>("50");
  const [reps, setReps] = useState<string>("10");
  const [sets, setSets] = useState<string>("3");
  const [note, setNote] = useState<string>("");
  const mutation = api.workout.add.useMutation();
  const send = async () => {
    await mutation
      .mutateAsync({
        userId: sessionData?.user?.id || "",
        date: new Date(date).toISOString(),
        weight: parseFloat(weight),
        reps: parseInt(reps),
        sets: parseInt(sets),
        note: note,
        exerciseId: selectedExerciseId,
      })
      .then(({ id }) => {
        return Router.push(`/workout/${id}`);
      })
      .catch(() => {
        return;
      });
  };
  const handleExerciseClick = useCallback((exerciseId: number) => {
    selectExerciseId(exerciseId);
  }, []);
  return (
    <>
      <Head>
        <title>EveryWorkout</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <Navigation />
        <div className="grid md:grid-cols-12">
          <div className="md:col-span-6 md:col-start-4">
            {mutation.isError && (
              <p className="mb-2 rounded-lg bg-red-100 p-4 text-red-900">
                エラーが発生しました: {mutation.error.data.path}
              </p>
            )}
            <div className="mb-2">
              <label
                className="mb-2 block text-sm font-bold text-gray-700"
                htmlFor="date"
              >
                日付
              </label>
              <input
                className="focus:shadow-outline w-full appearance-none rounded border py-2 px-3 leading-tight text-gray-700 shadow focus:outline-none"
                id="date"
                type="date"
                placeholder="日付"
                value={date}
                onChange={(e) => setDate(e.target.value)}
              />
            </div>
            <div className="mb-2">
              <label className="block text-sm font-bold text-gray-700">
                種目
              </label>
              <ExerciseSelector
                selectedExerciseId={selectedExerciseId}
                handleExerciseClick={handleExerciseClick}
              />
            </div>
            <div className="mb-2">
              <label
                className="mb-2 block text-sm font-bold text-gray-700"
                htmlFor="weight"
              >
                重量
              </label>
              <input
                className="focus:shadow-outline w-full appearance-none rounded border py-2 px-3 leading-tight text-gray-700 shadow focus:outline-none"
                id="weight"
                type="number"
                step="2.5"
                placeholder="重量"
                value={weight}
                onChange={(e) => setWeight(e.target.value)}
              />
            </div>
            <div className="mb-2">
              <label
                className="mb-2 block text-sm font-bold text-gray-700"
                htmlFor="reps"
              >
                rep数
              </label>
              <input
                className="focus:shadow-outline w-full appearance-none rounded border py-2 px-3 leading-tight text-gray-700 shadow focus:outline-none"
                id="reps"
                type="number"
                placeholder="rep数"
                value={reps}
                onChange={(e) => setReps(e.target.value)}
              />
            </div>
            <div className="mb-2">
              <label
                className="mb-2 block text-sm font-bold text-gray-700"
                htmlFor="sets"
              >
                セット数
              </label>
              <input
                className="focus:shadow-outline w-full appearance-none rounded border py-2 px-3 leading-tight text-gray-700 shadow focus:outline-none"
                id="sets"
                type="number"
                placeholder="セット数"
                value={sets}
                onChange={(e) => setSets(e.target.value)}
              />
            </div>
            <div className="mb-2">
              <label
                className="mb-2 block text-sm font-bold text-gray-700"
                htmlFor="username"
              >
                メモ
              </label>
              <input
                className="focus:shadow-outline w-full appearance-none rounded border py-2 px-3 leading-tight text-gray-700 shadow focus:outline-none"
                id="note"
                type="text"
                placeholder="メモ"
                value={note}
                onChange={(e) => setNote(e.target.value)}
              />
            </div>
            {!mutation.isLoading && (
              <Button onClick={() => void send()}>登録</Button>
            )}
            {mutation.isLoading && <Loading />}
          </div>
        </div>
      </main>
    </>
  );
};

export default AddWorkout;
